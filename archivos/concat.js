// concat(): El método concat() combina dos o más arrays, devolviendo un nuevo array
// que contiene los elementos de ambos arrays. No altera los arrays originales.

const array1 = [1, 2, 3];
const array2 = [4, 5, 6];

const combinedArray = array1.concat(array2);

console.log(combinedArray); // Output: [1, 2, 3, 4, 5, 6]
console.log(array1); // Output: [1, 2, 3]
console.log(array2); // Output: [4, 5, 6]
